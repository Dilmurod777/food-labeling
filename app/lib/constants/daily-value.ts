export function getDVByName(name: string, type: string): string {
  name = name.replace("-", "_").toUpperCase();
  type = type.trim().toUpperCase();

  if (type == "DEFAULT") type = "";

  try {
    return eval(type ? `DV_${name}_${type}` : `DV_${name}`) || "1";
  } catch (e) {
    return "1";
  }
}
